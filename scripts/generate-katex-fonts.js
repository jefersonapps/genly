const fs = require('fs');
const path = require('path');

const fontsDir = path.join(__dirname, '../src/lib/katex/fonts');
const outputFile = path.join(__dirname, '../src/lib/katex/katex-fonts.ts');
const cssFile = path.join(__dirname, '../src/lib/katex/katex.min.css');

const css = fs.readFileSync(cssFile, 'utf8');
const fontFaces = css.match(/@font-face\s*\{[^}]+\}/g) || [];

let resultStr = "// Auto-generated by scripts/generate-katex-fonts.js\n\n";
resultStr += "export const KatexFontFaces = `\n";

for (const fontFace of fontFaces) {
  const familyMatch = fontFace.match(/font-family:([^;]+)/);
  if (!familyMatch) continue;
  let family = familyMatch[1].trim().replace(/['"]/g, '');

  const weightMatch = fontFace.match(/font-weight:([^;]+)/);
  let weight = weightMatch ? weightMatch[1].trim() : 'normal';

  const styleMatch = fontFace.match(/font-style:([^;]+)/);
  let style = styleMatch ? styleMatch[1].trim() : 'normal';

  const ttfMatch = fontFace.match(/url\(([^)]+\.ttf)\)/);
  if (!ttfMatch) continue;
  const ttfUrl = ttfMatch[1].replace(/['"]/g, '');
  
  const ttfPath = path.join(fontsDir, ttfUrl.replace('fonts/', ''));
  if (fs.existsSync(ttfPath)) {
    const base64 = fs.readFileSync(ttfPath).toString('base64');
    resultStr += "  @font-face { font-family: '" + family + "'; src: url('data:font/ttf;base64," + base64 + "') format('truetype'); font-weight: " + weight + "; font-style: " + style + "; }\n";
  }
}

resultStr += "`;\n";

fs.writeFileSync(outputFile, resultStr);
console.log('Successfully generated katex-fonts.ts');
